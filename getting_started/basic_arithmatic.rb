# Basic arithmetic operations in Ruby
# Addition
puts 2 + 3  # Output: 5

# submission
puts 10 - 5  # Output: 5

# Multiplication
puts 4 * 3  # Output: 12

# Division
puts 20 / 4  # Output: 5

# Modulus (remainder)
puts 10 % 3  # Output: 1

# Exponentiation
puts 2 ** 3  # Output: 8

# PEMDAS/BODMAS order of operations
# Parentheses, Exponents, Multiplication and Division (from left to right), Addition and Subtraction (from left to right)
puts (2 + 3) * 4  # Output: 20
puts 2 + 3 * 4  # Output: 14
puts 10 - 4 / 2  # Output: 8
puts 10 - (4 / 2)  # Output: 8
puts 3 + 4 ** 2  # Output: 19
puts (3 + 4) ** 2  # Output: 49