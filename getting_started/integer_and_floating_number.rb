# Integer numbers are whole numbers without a decimal point.

puts 100 # positive integer
puts(-42) # negative integer (best practice to use parentheses)
puts 0 # zero
puts 1_000 # underscores can be used for better readability (same like PHP's numeric literals)

# Floating-point numbers (or floats) are numbers with a decimal point.
puts 3.14 # positive float
puts(-0.001) # negative float
puts(-10.001) # negative float with trailing zeros
puts 0.0 # float representation of zero