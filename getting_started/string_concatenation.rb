# String concatenation in Ruby can be done in several ways. Here are some common methods:
# Using the + operator
puts "Hello, " + "world!"  # Output: Hello, world!

# Using the << operator (shovel operator)
puts "Hello, " << "world!"  # Output: Hello, world!

# Using string methods (e.g., concat method)
puts "Hello, ".concat("world!")  # Output: Hello, world!