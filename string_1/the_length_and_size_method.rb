# The lenght and size method are used to determine the number of characters in a string.
# Both methods return the same result and can be used interchangeably.

name = "Veez"
puts name.length  # Output: 4
puts name.size    # Output: 4

puts "   .halo2$".size  # Output: 7 (includes spaces and special characters)
puts "".length       # Output: 0 (empty string has length 0)