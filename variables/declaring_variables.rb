# Declaring a variable in Ruby
# Same with other's languages, we can declare a variable by simply assigning a value to it.
# The ruby language is similar to python in this aspect.

age = 25
puts age

first_name = "Muhammad"
last_name = "Luthfi"
puts first_name + " " + last_name

age_next_year = age + 1
puts "Next year, I will be #{age_next_year} years old." # Using string interpolation
